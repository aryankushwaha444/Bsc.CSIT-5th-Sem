//  control-flow
const age = 18;

if (age) {
  console.log("Age found");
} else {
  console.log("Age Not found");
}

if (!age) {
  console.log("Age Not found");
} else {
  console.log("Age found");
}

if (age > 16) {
  console.log("You are eligible to Vote");
} else {
  console.log("You are not eligible to Vote");
}


